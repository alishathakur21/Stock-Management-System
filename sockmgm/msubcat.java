    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String catname=jTable1.getValueAt(jTable1.getSelectedRow(),1 ).toString();
        try{

            Connection myconnection=DriverManager.getConnection(dburl,dbuser,dbpass);
            String q="select * from category where catname=? ";
            PreparedStatement myst=myconnection.prepareStatement(q);
            myst.setString(1,catname);
            ResultSet res=myst.executeQuery();
            if(res.next())
            {
                jTextField2.setText(res.getString("catname"));
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
            }

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Error " + e.getMessage());
        }

    }//GEN-LAST:event_jTable1MouseClicked

