    void fetchcat()
    {
         try
        {
            Connection myconn = DriverManager.getConnection(dburl,dbuser,dbpass);
            try
            {
                String qu="select * from category";
                PreparedStatement myst = myconn.prepareStatement(qu);
               
                ResultSet res=myst.executeQuery();
                if(res.next())
                {
                    do
                    {
                       jComboBox1.addItem(res.getString("catid")+","+res.getString("catname"));
                    }while(res.next());
                }
                else
                {
                     jComboBox1.addItem("No category available");
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(rootPane, "Error " + e.getMessage());
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Error " + e.getMessage());
        }
    }

